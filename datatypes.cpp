#include<iostream>

int main() {

    //integer (whole number)
    int age = 21;
    int year = 2024;
    int days = 7.5;
    std::cout<<age<<"\n";
    std::cout<<year<<"\n";
    std::cout<<days<<"\n";

    //double (integers with decimal)
    double price = 10.99;
    double gpa = 2.5;
    double temperature = 25.1;
    std::cout<<price<<"\n";
    std::cout<<gpa<<"\n";
    std::cout<<temperature<<"\n";

    //single character
    char grade = 'A';
    char initials = 'B';
    std::cout<<grade<<"\n";
    std::cout<<initials<<"\n";

    //boolean (true or false)
    bool student = true;
    bool powered = false;
    std::cout<<student<<"\n";
    std::cout<<powered<<"\n";

    //string (object that represents a sequence of text)
    std::string name = "Bro";
    std::cout<<"Hello "<<name<<"\n";

    return 0;
}